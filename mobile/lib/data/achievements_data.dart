int getUnlockedAchievementCount(PlayerProgress progress) {
  return achievementCatalog.where((a) => a.isUnlocked(progress)).length;
}

List<Achievement> getPreviewAchievements(
  PlayerProgress progress, {
  int limit = 4,
}) {
  final unlocked = achievementCatalog
      .where((a) => a.isUnlocked(progress))
      .toList()
      .reversed
      .toList();

  final locked = achievementCatalog.where((a) => !a.isUnlocked(progress)).toList();

  return [...unlocked, ...locked].take(limit).toList();
}

Map<String, List<Achievement>> groupAchievementsBySection() {
  final Map<String, List<Achievement>> grouped = {};

  for (final achievement in achievementCatalog) {
    grouped.putIfAbsent(achievement.sectionTitle, () => <Achievement>[]);
    grouped[achievement.sectionTitle]!.add(achievement);
  }

  return grouped;
}